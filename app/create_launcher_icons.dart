import 'dart:io';
import 'dart:typed_data';

void main() {
  // A minimal 1x1 transparent PNG (valid PNG file)
  final bytes = Uint8List.fromList([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
    0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0x8C, 0x61, 0x00, 0x00, 0x00,
    0x0D, 0x49, 0x44, 0x41, 0x54, 0x08, 0xD7, 0x63, 0xFC, 0xCF, 0xC0, 0x00,
    0x00, 0x00, 0x02, 0x00, 0x01, 0xE2, 0x21, 0xBC, 0x33, 0x00, 0x00, 0x00,
    0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
  ]);

  final dirs = ['mipmap-mdpi', 'mipmap-hdpi', 'mipmap-xhdpi', 'mipmap-xxhdpi', 'mipmap-xxxhdpi'];
  final base = Directory('android/app/src/main/res');

  for (final dir in dirs) {
    final path = '${base.path}/$dir';
    Directory(path).createSync(recursive: true);
    File('$path/ic_launcher.png').writeAsBytesSync(bytes);
  }

  // Also create mipmap/ic_launcher.png (fallback)
  final fallbackDir = Directory('${base.path}/mipmap');
  fallbackDir.createSync(recursive: true);
  File('${fallbackDir.path}/ic_launcher.png').writeAsBytesSync(bytes);

  print('Done: created launcher icons in all mipmap folders');
}
